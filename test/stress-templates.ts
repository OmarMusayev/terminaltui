// ─── All Templates ────────────────────────────────────────

const allTemplates: TemplateInfo[] = [
  buildMinimalConfig(),
  buildPortfolioConfig(),
  buildLandingConfig(),
  buildRestaurantConfig(),
  buildBlogConfig(),
  buildCreativeConfig(),
];

// ─── Per-Template Tests ───────────────────────────────────

interface TemplateSummary {
  template: string;
  total: number;
  passed: number;
  failed: number;
  tests: TestResult[];
}

const summaries: TemplateSummary[] = [];

for (const tpl of allTemplates) {
  const templateResults: TestResult[] = [];

  function ttest(name: string, fn: () => void): void {
    const fullName = `[${tpl.name}] ${name}`;
    try {
      fn();
      templateResults.push({ name: fullName, passed: true });
      results.push({ name: fullName, passed: true });
    } catch (err: any) {
      templateResults.push({ name: fullName, passed: false, error: err.message });
      results.push({ name: fullName, passed: false, error: err.message });
    }
  }

  // Test 1: defineSite() does not throw
  let site: Site | null = null;
  ttest("defineSite() parses without error", () => {
    site = defineSite(tpl.config);
    assert(!!site, "defineSite should return a Site object");
  });

  if (!site) {
    summaries.push({
      template: tpl.name,
      total: 1,
      passed: 0,
      failed: 1,
      tests: templateResults,
    });
    continue;
  }

  // Test 2: Page count matches expected
  ttest(`page count is ${tpl.expectedPages}`, () => {
    assert(
      tpl.config.pages.length === tpl.expectedPages,
      `Expected ${tpl.expectedPages} pages, got ${tpl.config.pages.length}`
    );
  });

  // Test 3: All page IDs are unique and non-empty
  ttest("page IDs are unique and non-empty", () => {
    const ids = new Set<string>();
    for (const pg of tpl.config.pages) {
      assert(!!pg.id, `Page missing id: ${pg.title}`);
      assert(!ids.has(pg.id), `Duplicate page id: ${pg.id}`);
      ids.add(pg.id);
    }
  });

  // Test 4: Theme is valid
  ttest("theme is valid", () => {
    const theme = tpl.config.theme as Theme;
    assert(!!theme, "Theme should be defined");
    assert(!!theme.accent, "Theme missing accent color");
    assert(!!theme.text, "Theme missing text color");
    assert(!!theme.border, "Theme missing border color");
    assert(!!theme.muted, "Theme missing muted color");
  });

  // Test 5: Banner renders
  if (tpl.hasBanner) {
    ttest("banner renders", () => {
      const banner = tpl.config.banner!;
      const bannerLines = renderBanner(banner.text, { font: banner.font });
      assert(bannerLines.length > 0, "Banner should produce output lines");
      const hasContent = bannerLines.some(l => l.trim().length > 0);
      assert(hasContent, "Banner should have non-empty content");
    });

    if (tpl.bannerGradient) {
      ttest("banner gradient applies", () => {
        const banner = tpl.config.banner!;
        const bannerLines = renderBanner(banner.text, { font: banner.font });
        const colored = gradientLines(bannerLines, tpl.bannerGradient!);
        assert(colored.length === bannerLines.length, "Gradient should preserve line count");
        // Gradient lines should contain ANSI escape codes
        const hasAnsi = colored.some(l => l.includes("\x1b["));
        assert(hasAnsi, "Gradient lines should contain ANSI color codes");
      });
    }
  }

  // Test 6: All pages render at width 80 without overflow
  ttest("all pages render at width 80", () => {
    const ctx = createTestContext(80, tpl.config.theme as Theme);
    for (const pg of tpl.config.pages) {
      for (const block of pg.content) {
        const lines = renderBlock(block, ctx);
        assert(Array.isArray(lines), `Block type '${block.type}' on page '${pg.id}' should return array`);
        // Non-spacer blocks should produce output
        if (block.type !== "spacer") {
          assert(lines.length > 0, `Block type '${block.type}' on page '${pg.id}' should produce output at width 80`);
        }
      }
    }
  });

  // Test 7: Check for overflow at width 80
  ttest("no overflow at width 80", () => {
    const ctx = createTestContext(80, tpl.config.theme as Theme);
    for (const pg of tpl.config.pages) {
      for (const block of pg.content) {
        const lines = renderBlock(block, ctx);
        for (let i = 0; i < lines.length; i++) {
          const plainLen = stripAnsi(lines[i]).length;
          if (plainLen > 82) { // +2 tolerance
            throw new Error(
              `Overflow on page '${pg.id}', block '${block.type}', line ${i}: ` +
              `${plainLen} chars (max 80). Content: "${stripAnsi(lines[i]).substring(0, 60)}..."`
            );
          }
        }
      }
    }
  });

  // Test 8: Check for overflow at width 40
  ttest("no overflow at width 40", () => {
    const ctx = createTestContext(40, tpl.config.theme as Theme);
    for (const pg of tpl.config.pages) {
      for (const block of pg.content) {
        const lines = renderBlock(block, ctx);
        for (let i = 0; i < lines.length; i++) {
          const plainLen = stripAnsi(lines[i]).length;
          if (plainLen > 42) { // +2 tolerance
            throw new Error(
              `Overflow on page '${pg.id}', block '${block.type}', line ${i}: ` +
              `${plainLen} chars (max 40). Content: "${stripAnsi(lines[i]).substring(0, 40)}..."`
            );
          }
        }
      }
    }
  });

  // Test 9: All content blocks produce output (non-spacer)
  ttest("all content blocks produce output", () => {
    const ctx = createTestContext(80, tpl.config.theme as Theme);
    let totalBlocks = 0;
    let producingBlocks = 0;
    for (const pg of tpl.config.pages) {
      for (const block of pg.content) {
        totalBlocks++;
        const lines = renderBlock(block, ctx);
        if (lines.length > 0) producingBlocks++;
        // Spacers produce empty lines which is their job
        if (block.type !== "spacer") {
          assert(lines.length > 0,
            `Block '${block.type}' on page '${pg.id}' produced 0 lines`);
        }
      }
    }
    assert(producingBlocks > 0, `At least some blocks should produce output (${producingBlocks}/${totalBlocks})`);
  });

  // Test 10: Animations config is valid (if present)
  if (tpl.hasAnimations) {
    ttest("animations config is valid", () => {
      const anims = tpl.config.animations!;
      assert(anims.boot === true, "boot should be true");
      const validTransitions = ["instant", "fade", "slide", "wipe"];
      assert(
        validTransitions.includes(anims.transitions!),
        `transitions should be one of ${validTransitions.join(", ")}, got '${anims.transitions}'`
      );
    });
  }

  // Test 11: Render at multiple widths (stress check)
  for (const width of [30, 60, 100, 120]) {
    ttest(`all blocks render at width ${width}`, () => {
      const ctx = createTestContext(width, tpl.config.theme as Theme);
      for (const pg of tpl.config.pages) {
        for (const block of pg.content) {
          const lines = renderBlock(block, ctx);
          assert(Array.isArray(lines), `Block '${block.type}' at width ${width} should return array`);
        }
      }
    });
  }

  // Test 12: Content text is preserved in render output
  ttest("content text is present in rendered output", () => {
    const ctx = createTestContext(80, tpl.config.theme as Theme);
    for (const pg of tpl.config.pages) {
      for (const block of pg.content) {
        const lines = renderBlock(block, ctx);
        const text = lines.map(l => stripAnsi(l)).join("\n");
        // Verify some content from the block appears in the output
        if (block.type === "text" && block.content.length > 0) {
          // For markdown/text blocks, at least part of the content should appear
          const snippet = block.content.substring(0, 10).replace(/[*_#]/g, "");
          if (snippet.trim().length > 3) {
            assert(
              text.includes(snippet.trim()),
              `Text content "${snippet.trim()}" not found in rendered output on page '${pg.id}'`
            );
          }
        }
        if (block.type === "card" && block.title.length > 0) {
          assert(
            text.includes(block.title),
            `Card title "${block.title}" not found in rendered output on page '${pg.id}'`
          );
        }
        if (block.type === "link") {
          assert(
            text.includes(block.label),
            `Link label "${block.label}" not found in rendered output on page '${pg.id}'`
          );
        }
      }
    }
  });

  // Collect summary
  const passed = templateResults.filter(r => r.passed).length;
  const failed = templateResults.filter(r => !r.passed).length;
  summaries.push({
    template: tpl.name,
    total: templateResults.length,
    passed,
    failed,
    tests: templateResults,
  });
}

// ─── Cross-Template Tests ─────────────────────────────────

test("Cross-template: all 6 templates build successfully", () => {
  assert(allTemplates.length === 6, `Expected 6 templates, got ${allTemplates.length}`);
  for (const tpl of allTemplates) {
    const site = defineSite(tpl.config);
    assert(!!site, `Template '${tpl.name}' should produce a valid site`);
  }
});

test("Cross-template: every template uses a different banner font or gradient", () => {
  const bannerKeys = allTemplates.map(t => {
    const g = t.bannerGradient ? t.bannerGradient.join(",") : "none";
    return `${t.bannerFont}|${g}`;
  });
  const unique = new Set(bannerKeys);
  assert(
    unique.size === allTemplates.length,
    `Expected ${allTemplates.length} unique banner configs, got ${unique.size}: ${[...unique].join("; ")}`
  );
});

test("Cross-template: all template names are unique", () => {
  const names = allTemplates.map(t => t.name);
  const unique = new Set(names);
  assert(unique.size === allTemplates.length, "Template names should be unique");
});

// ─── Summary Output ───────────────────────────────────────

const totalPassed = results.filter(r => r.passed).length;
const totalFailed = results.filter(r => !r.passed).length;

console.log("\n" + "=".repeat(70));
console.log("STRESS TEST: CLI Template Validation");
console.log("=".repeat(70));

// Per-template summary
for (const summary of summaries) {
  const status = summary.failed === 0 ? "PASS" : "FAIL";
  console.log(`\n--- Template: ${summary.template} [${status}] (${summary.passed}/${summary.total}) ---`);
  for (const r of summary.tests) {
    const icon = r.passed ? "\u2713" : "\u2717";
    const st = r.passed ? "PASS" : "FAIL";
    let line = `  ${icon} [${st}] ${r.name}`;
    if (r.error) line += `\n           Error: ${r.error}`;
    console.log(line);
  }
}

// Cross-template tests
console.log("\n--- Cross-Template Tests ---");
const crossTests = results.filter(r => !r.name.startsWith("["));
for (const r of crossTests) {
  const icon = r.passed ? "\u2713" : "\u2717";
  const st = r.passed ? "PASS" : "FAIL";
  let line = `  ${icon} [${st}] ${r.name}`;
  if (r.error) line += `\n           Error: ${r.error}`;
  console.log(line);
}

console.log("\n" + "=".repeat(70));
console.log(`TOTAL: ${results.length} | PASSED: ${totalPassed} | FAILED: ${totalFailed}`);
console.log("=".repeat(70));

if (totalFailed > 0) {
  console.log("\nFailed tests:");
  for (const r of results.filter(r => !r.passed)) {
    console.log(`  - ${r.name}: ${r.error}`);
  }
}

console.log("\n" + (totalFailed === 0 ? "ALL TESTS PASSED" : `${totalFailed} TEST(S) FAILED`) + "\n");
process.exit(totalFailed > 0 ? 1 : 0);
