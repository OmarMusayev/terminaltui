import { registry } from "../art-registry/index.js";
import type { IconData } from "../art-registry/types.js";

export function getIcon(name: string, size?: "small" | "medium" | "large"): string[] | undefined {
  // Check registry first
  const custom = registry.get("icon", name) as IconData | null;
  if (custom) {
    if (size === "small" && custom.small) return custom.small;
    if (size === "medium" && custom.medium) return custom.medium;
    if (size === "large" && custom.large) return custom.large;
    return custom.art ?? custom.medium ?? custom.small ?? custom.large;
  }

  // Fall back to built-in icons
  return icons[name];
}

